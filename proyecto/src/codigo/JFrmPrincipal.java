    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        
        try {
            Reader lector = new BufferedReader(new FileReader(chooser.getSelectedFile()));
            Lexer generador = new Lexer(lector);
            String resultado = "";
            while (true) {
                Tokens tokens = generador.yylex();
                if (tokens == null) {
                    resultado += "FIN DEL ANALISIS";
                    txtMostrar.setText(resultado);
                    return;
                }
                switch (tokens) {
                    case ERROR:
                        resultado += "simbolo no definido\n";
                        break;
                    case Reservadas: case Numero: case Identificador: case Etiqueta: case Etiqueta_Cierre: case Atributo: 
                        resultado += generador.lexema + ": Es un/a " + tokens + "\n";
                        break;
                        
                    default:
                        resultado += generador.lexema + ": Es un/a " + tokens + "\n";
                        break;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JFrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAnalizarActionPerformed

